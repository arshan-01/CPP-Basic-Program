#include <iostream>
#include <math.h>
using namespace std;
int main()
{
float n;
cout<<"Enter a floating point value:";
cin>>n;
cout<<"Trignometric cosine of "<<n<<" is = "<<cos(n)<<endl;
cout<<"Trignometric sine of "<<n<<" is = "<<sin(n)<<endl;
cout<<"Trignometric tangent of "<<n<<" is = "<<tan(n)<<endl;
cout<<"Natural log of "<<n<<" is = "<<log(n)<<endl;
cout<<"Base 10 log of "<<n<<" is = "<<log10(n)<<endl;
return 0;
}
